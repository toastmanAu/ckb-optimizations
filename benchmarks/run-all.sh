#!/bin/bash
# Master orchestration script for all Nervos optimization benchmarks.
#
# Runs each benchmark suite in sequence and produces a combined summary.
#
# Usage:
#   ./run-all.sh              # Run everything
#   ./run-all.sh --quick      # CKB in CI mode, skip nostd, skip heavy benchmarks
#   ./run-all.sh --skip=ckb   # Skip specific suites (comma-separated: ckb,vm,smt,molecule,debugger,lightclient)
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPORT_DIR="$SCRIPT_DIR/reports"
TIMESTAMP=$(date +%Y%m%d_%H%M%S)
SUMMARY="$REPORT_DIR/summary-$TIMESTAMP.md"

mkdir -p "$REPORT_DIR"

QUICK=0
SKIP=""
for arg in "$@"; do
    case "$arg" in
        --quick)    QUICK=1 ;;
        --skip=*)   SKIP="${arg#--skip=}" ;;
    esac
done

should_run() {
    local name="$1"
    if echo "$SKIP" | grep -qw "$name"; then
        return 1
    fi
    return 0
}

# ---------------------------------------------------------------------------
# Header
# ---------------------------------------------------------------------------
echo "================================================================"
echo "  Nervos Optimization Benchmark Suite"
echo "  $(date '+%Y-%m-%d %H:%M:%S %Z')"
echo "================================================================"
echo ""

OVERALL_START=$(date +%s)

declare -a SUITE_NAMES=()
declare -a SUITE_STATUS=()
declare -a SUITE_TIMES=()

run_suite() {
    local name="$1"
    local script="$2"
    shift 2

    if ! should_run "$name"; then
        echo ">>> Skipping $name (--skip)"
        echo ""
        return
    fi

    echo "================================================================"
    echo ">>> Running: $name"
    echo "================================================================"
    echo ""

    local start elapsed
    start=$(date +%s)

    SUITE_NAMES+=("$name")

    if "$script" "$@" 2>&1; then
        SUITE_STATUS+=("OK")
    else
        SUITE_STATUS+=("FAIL (exit $?)")
    fi

    elapsed=$(( $(date +%s) - start ))
    SUITE_TIMES+=("${elapsed}s")

    echo ""
    echo ">>> $name completed in ${elapsed}s"
    echo ""
}

# ---------------------------------------------------------------------------
# Run each suite
# ---------------------------------------------------------------------------

if [ "$QUICK" -eq 1 ]; then
    run_suite "ckb" "$SCRIPT_DIR/run-ckb-bench-quick.sh"
else
    run_suite "ckb" "$SCRIPT_DIR/run-ckb-bench.sh"
fi

run_suite "vm" "$SCRIPT_DIR/run-vm-bench.sh"

if [ "$QUICK" -eq 1 ]; then
    run_suite "smt" "$SCRIPT_DIR/run-smt-bench.sh" --report-only
else
    run_suite "smt" "$SCRIPT_DIR/run-smt-bench.sh"
fi

run_suite "molecule" "$SCRIPT_DIR/run-molecule-bench.sh"

if [ "$QUICK" -eq 1 ]; then
    run_suite "debugger" "$SCRIPT_DIR/run-debugger-bench.sh" --skip-build
else
    run_suite "debugger" "$SCRIPT_DIR/run-debugger-bench.sh"
fi

if [ "$QUICK" -eq 1 ]; then
    run_suite "lightclient" "$SCRIPT_DIR/run-lightclient-bench.sh" --skip-build
else
    run_suite "lightclient" "$SCRIPT_DIR/run-lightclient-bench.sh" --duration 60
fi

OVERALL_ELAPSED=$(( $(date +%s) - OVERALL_START ))

# ---------------------------------------------------------------------------
# Generate summary
# ---------------------------------------------------------------------------
{
    cat <<EOF
# Nervos Optimization Benchmark Summary

**Date:** $(date '+%Y-%m-%d %H:%M:%S %Z')
**Host:** $(hostname 2>/dev/null || echo unknown) ($(uname -r 2>/dev/null || echo unknown))
**CPU:** $(lscpu 2>/dev/null | grep 'Model name' | head -1 | sed 's/.*:\s*//' || echo unknown)
**Total runtime:** ${OVERALL_ELAPSED}s

---

## Suite Results

| Suite | Status | Duration |
|-------|--------|----------|
EOF

    for i in "${!SUITE_NAMES[@]}"; do
        echo "| ${SUITE_NAMES[$i]} | ${SUITE_STATUS[$i]} | ${SUITE_TIMES[$i]} |"
    done

    echo ""
    echo "---"
    echo ""
    echo "## Generated Reports"
    echo ""

    for report in "$REPORT_DIR"/*-report*.md "$REPORT_DIR"/molecule-bench*.md; do
        if [ -f "$report" ]; then
            echo "- [$(basename "$report")]($(basename "$report"))"
        fi
    done

    echo ""
    echo "---"
    echo ""
    echo "*Summary generated by run-all.sh at $(date '+%Y-%m-%d %H:%M:%S %Z')*"

} > "$SUMMARY"

echo "================================================================"
echo "  All benchmarks complete in ${OVERALL_ELAPSED}s"
echo "  Summary: $SUMMARY"
echo "================================================================"
echo ""

# Show status table
echo "Suite Results:"
for i in "${!SUITE_NAMES[@]}"; do
    printf "  %-12s %s (%s)\n" "${SUITE_NAMES[$i]}" "${SUITE_STATUS[$i]}" "${SUITE_TIMES[$i]}"
done
echo ""
echo "Reports in: $REPORT_DIR/"
ls -1 "$REPORT_DIR"/*.md 2>/dev/null | while read -r f; do echo "  $(basename "$f")"; done
